    def _safe_text(self, value, default="N/A"):
        try:
            if value is None:
                return default

            if isinstance(value, dict):
                parts = []
                for k, v in value.items():
                    parts.append(f"{k}: {v}")
                return " | ".join(parts)

            if isinstance(value, (list, tuple, set)):
                return " | ".join(str(x) for x in value)

            text = str(value).strip()

            if not text:
                return default

            return text
        except Exception:
            return default