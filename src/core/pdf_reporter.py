    # ---------------------------------------------------------
    # FOOTER
    # ---------------------------------------------------------
    def _draw_footer(self, canvas, doc):
        canvas.saveState()
        canvas.setFont("Helvetica", 8)
        canvas.setFillColor(colors.grey)
        footer_text = f"Generated by StegHunter - Hybrid Steganography Detection Framework | Page {doc.page}"
        canvas.drawString(20 * mm, 10 * mm, footer_text)
        canvas.restoreState()

    # ---------------------------------------------------------
    # SAFE FLOAT
    # ---------------------------------------------------------
    def _safe_float(self, value, default=0.0):
        try:
            if value is None:
                return default

            if isinstance(value, np.generic):
                value = float(value)

            value = float(value)

            if math.isnan(value) or math.isinf(value):
                return default

            return value
        except Exception:
            return default

    # ---------------------------------------------------------
    # CASE ID
    # ---------------------------------------------------------
    def _generate_case_id(self):
        return "SH-" + datetime.now().strftime("%Y%m%d-%H%M%S")

    # ---------------------------------------------------------
    # VERDICT
    # ---------------------------------------------------------
    def _derive_forensic_verdict(self, score: float) -> str:
        score = self._safe_float(score)

        if score >= 85:
            return "STEGANOGRAPHY DETECTED (CRITICAL)"
        elif score >= 70:
            return "HIGHLY SUSPICIOUS"
        elif score >= 50:
            return "MODERATELY SUSPICIOUS"
        elif score >= 25:
            return "LOW SUSPICION"
        else:
            return "NO STRONG EVIDENCE DETECTED"

    def _risk_label(self, score: float) -> str:
        score = self._safe_float(score)

        if score >= 80:
            return "CRITICAL"
        elif score >= 60:
            return "HIGH"
        elif score >= 40:
            return "MODERATE"
        elif score >= 20:
            return "LOW"
        return "MINIMAL"

    # ---------------------------------------------------------
    # SCORE METER
    # ---------------------------------------------------------
    def _score_meter(self, score: float) -> Table:
        score = self._safe_float(score)
        filled = int(score // 5)
        meter = "█" * filled + "░" * (20 - filled)

        data = [[f"{meter}   {score:.2f} / 100"]]

        t = Table(data, colWidths=[170 * mm])
        t.setStyle(TableStyle([
            ('BACKGROUND', (0,0), (-1,-1), colors.HexColor("#EAF3FB")),
            ('BOX', (0,0), (-1,-1), 1, colors.HexColor("#0B5394")),
            ('TEXTCOLOR', (0,0), (-1,-1), colors.HexColor("#0B5394")),
            ('FONTNAME', (0,0), (-1,-1), 'Helvetica-Bold'),
            ('FONTSIZE', (0,0), (-1,-1), 11),
            ('ALIGN', (0,0), (-1,-1), 'CENTER'),
            ('TOPPADDING', (0,0), (-1,-1), 6),
            ('BOTTOMPADDING', (0,0), (-1,-1), 6),
        ]))
        return t

    # ---------------------------------------------------------
    # FILE INTELLIGENCE
    # ---------------------------------------------------------
    def _collect_file_intelligence(self, image_path: str):
        with open(image_path, "rb") as f:
            raw = f.read()

        md5 = hashlib.md5(raw).hexdigest()
        sha256 = hashlib.sha256(raw).hexdigest()
        size = f"{round(Path(image_path).stat().st_size / 1024, 2)} KB"

        with Image.open(image_path) as img:
            dimensions = f"{img.width} x {img.height}"
            histogram = img.convert("L").histogram()

        total = sum(histogram)
        entropy = 0.0

        if total > 0:
            for h in histogram:
                if h != 0:
                    p = h / total
                    entropy -= p * math.log2(p)

        return {
            "md5": md5,
            "sha256": sha256,
            "size": size,
            "dimensions": dimensions,
            "entropy": f"{entropy:.3f}"
        }

    # ---------------------------------------------------------
    # FORENSIC REASONING TEXT
    # ---------------------------------------------------------
    def _extract_reasoning_text(self, analysis_results: dict) -> str:
        raw = analysis_results.get(
            "forensic_reasoning",
            analysis_results.get(
                "reasoning",
                "Hybrid detector fusion identified multiple low-level statistical anomalies across the submitted image. "
                "Detector evidence was aggregated using weighted forensic consensus methodology."
            )
        )
        return self._safe_text(raw)


    # ---------------------------------------------------------
    # PAYLOAD REGION TEXT
    # ---------------------------------------------------------
    def _extract_payload_region_text(self, analysis_results: dict) -> str:
        payload = analysis_results.get("payload_location", {})

        if isinstance(payload, dict) and payload:
            return self._safe_text(payload)

        return "No sharply isolated payload concentration zone was conclusively identified."
    
